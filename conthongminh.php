    <h3 id="tieude"><b>Ăn gì giúp con thông minh</b></h3>
	<div id="noidung">
	<br><br>
    <p>
    <b>Bên cạnh việc phát triển thể chất, chuyện làm sao để con thông minh luôn là mối quan tâm hàng đầu của ông bà cha mẹ.</b>
    <br>
    Thực tế này khiến không ít nhãn hàng thực phẩm chức năng và sữa công thức đẩy mạnh quảng bá về các sản phẩm phát triển thông minh. Tuy nhiên phụ huynh vẫn có thể giúp trẻ bồi bổ trí não bằng những thực phẩm dinh dưỡng có trong cuộc sống.
    <br>
    <b>4 nguyên tắc vàng để nuôi dưỡng não</b>
    <ul>
    <li>Loại bỏ những chất có hại cho não như: thuốc lá (hút thuốc lá thụ động ở trẻ em); chất kích thích (cafein, nước ngọt, cà phê, rượu bia, chất cồn); tránh xa các chất nhuộm màu thực phẩm nhân tạo, thực phẩm giải khát nhiều đường tinh chế.</li>
    <li>Tăng cường tiêu thụ dưỡng chất thiết yếu cho não.
        <br>
        Cân bằng giữa 3 dưỡng chất chính là tinh bột, đường, chất béo, chất đạm.
        <br>
        Thiết kế chế độ ăn, thời gian ăn uống, nghĩ ngơi hợp lý, tập luyện kỹ năng ăn tốt góp phần không nhỏ để giúp trẻ thông minh.</li>
    </ul>
    <b>Lựa chọn thực phẩm cho trí não</b>
    <br>
    Cần lựa chọn những loại thực phẩm chứa chất béo omega 3. Cá vẫn được xem là thực phẩm của trí não. Cá có chứa chất béo omega 3 với thành phần chính yếu cho sự phát triển não bộ thai nhi và trẻ nhũ nhi. Chất DHA (docosahexaenoic acid) tham gia vào tính ổn định và phát triển màng tế bào não và sự myelin hoá dẫn truyền thần kinh. Chất này dễ dàng truyền từ người mẹ mang thai qua dinh dưỡng nhau thai và sữa mẹ thời kỳ cho con bú. Chất béo omega -3 có tự nhiên ở nhiều loại cá như: cá thu, cá hồi, cá cơm, cá ngừ và một số loại hạt như hạt đậu nành, óc chó, hạt nhân…
    <br>
    <img src="image/thongminh.png">
    <br>
    <b>Loại đường nào có lợi cho não?</b>
    <br>
    Đường glucose là nguồn cung cấp năng lượng chính cho não. Việc bổ sung thực phẩm nhiều đường/ ngọt làm gia tăng chỉ số đường huyết nhanh sẽ ảnh hưởng đến hành vi hoạt động trong ngày như buồn ngủ hoặc tăng hoạt động. Bổ sung thực phẩm ít ngọt/ không làm gia tăng chỉ số đường huyết cao sẽ giúp trí não làm việc điều hoà hơn.
    <br>
    Đường fructose trong trái cây (táo, bưởi, cam, nho) không gây tăng nhanh và làm cao chỉ số đường huyết nên khá tốt cho hoạt động não được điều hòa. Ăn nguyên trái có nhiều lợi điểm hơn việc uống nước ép bỏ bã đơn thuần. Ngoài đường fructose, trong vỏ, da của trái cây còn có chất tự nhiên polyphenol là chất rất tốt cho não.
    <br>
    <img src="image/thongminh2.png">
    <br>
    <b>Chất acid amin nào cần thiết cho não</b>
    Acid amin là đơn vị cấu trúc của chất đạm trong thực phẩm hằng ngày. Não cần acid amin để chế tạo các chất dẫn truyền thần kinh và cung cấp năng lượng. Hai loại acid amin quan trọng cho trí não là tryptophan và tyrosine.Tryptophan còn là tiền thân của hoạt chất làm dịu thần kinh serotonin, cơ thể không thể tạo ra tryptophan được, nên cần cung cấp qua thực phẩm. Tyrosine cần để sản xuất chất dẫn truyền hưng phấn trí não là dopamin, epinephrine/norepinephrine, cơ thể không thể tạo ra được tyrosine nếu đạm trong cơ thể không đầy đủ.
    <br>
    Thiếu tryptophan gây ảnh hưởng lớn đến tinh thần, suy giảm trí nhớ và dễ cáu giận. Thực phẩm giàu tryptophan là chuối, đậu phộng, hạt sen, gạo, thịt gà tây, bí đỏ.
    <br>
    Khẩu phần ăn giàu tyrosin giúp tỉnh táo, giảm căng thẳng và mệt mỏi. Tyrosin dễ dàng bổ sung qua 9 loại thực phẩm: tảo lục xoắn, đậu nành, trứng gà, phomat, cá, thịt gia cầm, ngũ cốc nguyên cám, hạt (bí, chia, hướng dương...).
    <br>
    Não rất cần vitamin và khoáng chất và nước.
    <br>
    <b>Vitamin B6:</b> điều hòa sản xuất dẫn truyền thần kinh kiểm soát ngủ, nghỉ, cảm xúc, duy trì chức năng bình thường của não bộ.Vitamin B6 có nhiều trong: thịt, cá, gan, đậu, chuối, bơ...
    <br>
    <b>Vitamin B12:</b> cần thiết cho vỏ bọc sợi thần kinh. Có nhiều trong thịt bò, gà, lòng đỏ trứng, phomat.
    <br>
    <b>Vitamin C:</b> tham gia sản xuất dẫn truyền thần kinh.
    <br>
    <b>Acid Folic:</b> chức năng dẫn truyền tín hiệu thần kinh: rau lá xanh, hạt đậu, thịt heo, thịt gà, tôm cá cua, sò hến.
    <br>
    <b>Canxi:</b> tiếp nhận dẫn truyền tín hiệu thần kinh: sữa và chế phẩm từ sữa, hạt ngũ cốc, cá, tôm, trứng, đậu nành.
    <br>
    <b>Kali:</b> cần cho sự dẫn truyền tín hiệu thần kinh: cam, chuối, khoai tây, trái cây khô, sữa, nước dừa.
    <br>
    <b>Sắt:</b> cần cho sự phát triển lành mạnh của tế bào thần kinh và dẫn truyền tín hiệu, thiếu sắt con người trở nên kém lanh lợi.
    <br>
    <b>Selen:</b> chất chống oxy hóa, giúp cơ thể ngăn chặn ung thư, trì hoãn tiến trình lão hóa. Thiếu selen có thể là một rủi ro đưa đến hội chứng chậm phát triển trí não. Selen có nhiều trong: cá, sò, hến, thịt động vật, ngũ cốc, trứng, gan.
    <br>
    Nước rất cần cho cơ thể nói chung và não bộ nói riêng. Cần cung cấp đủ nước phù hợp khuyến cáo theo lứa tuổi.
    <br>
    Ăn đúng giờ, đúng bữa. Không để quá đói mới ăn hoặc bỏ bữa, hoặc ăn quá no sẽ giúp não duy trì hoạt động điều hòa, khả năng tập trung tốt hơn.
    <br>Điểm tâm là bữa ăn quan trọng nhất trong ngày cho não bộ. Điểm tâm mang nhiều sức mạnh cho trí não như thực phẩm có nhiều chất xơ, ít tinh bột/ đường, các loại hạt còn vỏ, một ít chất đạm cần thiết.
    <br>Về trọng lượng cơ thể và trí não, nhiều kết quả nghiên cứu chỉ ra, béo phì là rủi ro đưa tới sự sa sút trí tuệ, bệnh tim mạch, bệnh lý rối loạn chuyển hóa và ung thư.
    <br>Tóm lại không quá khó tìm công thức dinh dưỡng giúp con thông minh phải không nào. Chìa khóa vàng không phải sữa nào, thuốc nào bổ não mà là dinh dưỡng lành mạnh, đa dạng phong phú, bên cạnh các hoạt động xã hội, giáo dục, học tập, rèn luyện thể chất, kỹ năng, sẽ dễ dàng mang lại một trí tuệ sáng suốt mà thôi.
    
