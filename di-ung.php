    <h3 id="tieude"><b>Dị ứng thực phẩm ở trẻ</b></h3>
	<div id="noidung">
	<br><br>
    <p>
    Trong khi hầu hết trẻ em đều có thể ăn nhiều thực phẩm khác nhau mà không gặp bất kỳ rắc rối nào, một số ít trẻ nhỏ lại bị dị ứng thức ăn. Dị ứng thức ăn tưởng chừng không có gì nguy hiểm nhưng lại có thể gây tử vong. Do đó, các bậc cha mẹ nên làm quen với các triệu chứng cũng như tìm hiểu cách điều trị khẩn cấp khi bé bị dị ứng.
    <br>
    <img src="image/thongminh.png">
    <br>
    <b>Dấu hiệu của dị ứng thực phẩm </b>
    <br>
    Khi sử dụng một thực phẩm nào đó, ngay cả với một lượng rất nhỏ, ta có thể bị các phản ứng dị ứng như nổi mẩn ở da, chảy mũi hay ngứa mắt. Một loại thực phẩm nào đó có thể gây phản ứng dị ứng được gọi là dị nguyên thực phẩm, nó thường là thành phần protein (đạm) trong thực phẩm.Cơ thể phản ứng với các thực phẩm gây dị ứng có thể nhanh hoặc chậm, sau vài phút, thậm chí vài giờ. Các triệu chứng thường biểu hiện nhẹ và mạn tính hơn là xuất hiện đột ngột, tuy nhiên, triệu chứng dị ứng ở mỗi người khác nhau.
    </p>
    <p>
    Dị ứng không phải là bệnh di truyền. Tuy nhiên, nếu một trong 2 người (bố hoặc mẹ) bị dị ứng, nguy cơ dị ứng ở các con là 50%; nếu cả bố và mẹ bị dị ứng, nguy cơ dị ứng ở con lên đến 75%.
    </p>
    <b>Một số thực phẩm dễ gây dị ứng cho trẻ</b>
    <p><b>Hải sản:</b> Các loại hải sản là tác nhân gây dị ứng nhiều nhất, bao gồm một số loại cá có hàm lượng thủy ngân cao (cá trích, cá chình, lươn, cá tầm, cá ngừ, cá thu...), nghêu sò, tôm, cua...</p>
    <p><b>Thực phẩm chua:</b> Một số loại trái cây rất tốt cho sức khỏe như dâu tây, cà chua... lại có thể là tác nhân gây dị ứng thực phẩm ở trẻ do hàm lượng axít cao. Nhiều trẻ ăn những thực phẩm này thường bị mẩn đỏ quanh miệng. Đây là loại dị ứng nhẹ, các chuyên gia dinh dưỡng khuyến cáo trẻ dưới một tuổi không nên ăn đồ chua.</p>
    <p><b>Trứng:</b>Trong lòng trắng trứng gà có một loại protein có thể gây dị ứng cho một số bé mẫn cảm với loại chất này.</p>
    <p><b>Một số loại rau xanh:</b>Một số loại rau xanh có hàm lượng nitrat cao có thể phản ứng không tốt với cơ địa của trẻ nhỏ. Do vậy, trẻ nhỏ nên thận trọng khi ăn củ cải, cà rốt, củ cải xanh…</p>
    <p><i>Ngũ cốc:</i>Trong số ngũ cốc có thể gây dị ứng thì lạc là nguyên nhân gây hậu quả xấu đặc biệt với trẻ bị hen suyễn. Sau đó là đậu nành và lúa mì.</p>
    <p><i>Sữa bò:</i>Dị ứng sữa bò cũng là tình trạng hay gặp. Đó là tình trạng cơ thể của trẻ mẫn cảm với thành phần đạm sữa bò, gây ra phản ứng dị ứng. Dị ứng xảy ra có thể rất đột ngột: nôn mửa, thở khò khè, nổi ban đỏ, mặt sưng phù, hay nặng hơn là phản ứng phản vệ toàn thân. Cũng có trường hợp dị ứng diễn ra khó phát hiện hơn như trẻ khó chịu, nôn mửa, đau bụng, tiêu chảy...</p>
    <b>Phân biệt dị ứng thực phẩm với không dung nạp thực phẩm</b>
    <p>Đôi khi phản ứng khó chịu với một số loại thực phẩm xảy ra ở trẻ sơ sinh và trẻ nhỏ không hẳn là dị ứng, đó là triệu chứng không tiêu hóa được thực phẩm. Nguyên nhân của triệu chứng này có thể là do cơ thể không có khả năng tiêu hóa một số thành phần nhất định của loại thức ăn đó, nó không liên quan đến phản ứng của hệ miễn dịch. Triệu chứng này không nguy hiểm đến tính mạng.Không dung nạp lactose là một ví dụ điển hình của triệu chứng này. Chính vì vậy, khi có biểu hiện của dị ứng thực phẩm, cần đưa trẻ tới các cơ sở y tế để được thăm khám, xác định chính xác trẻ bị dị ứng thức ăn hay chỉ đơn thuần là không dung nạp thức ăn.</p>
    <b>Cách phòng tránh dị ứng cho trẻ</b>
    <p>Những trẻ được sinh ra trong gia đình có người mắc các bệnh dị ứng sẽ dễ bị dị ứng thức ăn nên cần được bú sữa mẹ hoàn toàn trong 6 tháng đầu. Khi trẻ bước sang tuổi ăn dặm, không nên vội vã cho ăn nhiều loại thức ăn mới cùng lúc. Nên bắt đầu với các thức ăn ít dị ứng như gạo và các loại củ. Tránh cho trẻ dùng các loại thức ăn chế biến theo lối công nghiệp như thịt lợn xông khói, thịt lợn muối, các chất nhuộm màu, gia vị nhân tạo.</p>
    <p>Nên tham khảo ý kiến bác sĩ chuyên khoa dị ứng khi bạn nghi ngờ con mình dị ứng với một loại thức ăn nào đó. Khi biết trẻ dị ứng với một loại thức ăn nào đó, nên loại bỏ nó ra khỏi thực đơn của trẻ. Không nên chế biến hoặc đựng thức ăn của trẻ trong các dụng cụ có dính các thức ăn mà trẻ dị ứng.
       <br>Tuy nhiên, dị ứng thức ăn không kéo dài suốt cả đời, chính vì thế bạn không cần bắt trẻ kiêng khem kéo dài một loại thực phẩm nào cả. Sau một thời gian, bạn có thể cho trẻ ăn lại thức ăn đó (ngoại trừ những món gây phản ứng dị ứng cấp tính như sốc phản vệ).</p>
